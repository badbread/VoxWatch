"""
audio.py — Audio Test and Preview API Router

Endpoints:
    POST /api/audio/test               — Trigger a test audio push to a camera via go2rtc
    POST /api/audio/preview            — Generate a deterrent message sample and return
                                         raw WAV audio for in-browser playback
    POST /api/audio/test-tts-provider  — Validate cloud TTS provider credentials (no audio)

The test endpoint is intended for setup verification: it pushes a short test
tone or a canned message to a camera's speaker so the user can confirm the
audio path works without triggering a real detection event.

The preview endpoint synthesizes a deterrent message using the requested TTS
provider and persona, then streams the WAV back to the browser so the user
can hear what the combination sounds like without pushing anything to a
camera speaker.  This is a "try before you buy" feature for voice / persona
selection in the dashboard config UI.

For dispatch personas (``police_dispatch``, ``tony_montana_dispatch``), the
preview request is **proxied to the VoxWatch Preview API** (port 8892 by
default) so the full dispatch experience — channel intro, random chatter,
multi-segment TTS, radio effects, officer response — is generated by the same
code that runs during live detection events.  If the VoxWatch service is
unreachable the endpoint falls back to local synthesis so the user always
hears something.

WARNING: The test push calls go2rtc directly using the same mechanism as
VoxWatch. Ensure the camera speaker volume and message content are appropriate
before calling this endpoint.

Security:
    The camera_name field in the request body is validated against a strict
    allowlist pattern before use.  This prevents SSRF attacks where a crafted
    camera name could direct go2rtc to fetch audio from or push audio to an
    unintended internal host.  See the _validate_camera_name helper for details.
"""

