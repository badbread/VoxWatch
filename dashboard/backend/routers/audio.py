async def preview_audio(request: PreviewRequest) -> StreamingResponse:
    """Synthesize a deterrent message sample and stream WAV back to the browser.

    The caller selects a persona and TTS provider.  If no custom message is
    supplied, a pre-written sample for that persona is used so the user hears
    a realistic deterrent rather than a bland test phrase.

    Synthesis is performed inside a temporary file so the WAV can be read back
    after the subprocess/HTTP call finishes.  The temp file is deleted after
    the response is streamed.

    For cloud providers (ElevenLabs, OpenAI, Cartesia), if synthesis fails due
    to a missing or invalid API key, the endpoint falls back to espeak rather
    than returning an error, so the user always hears something.  The response
    headers X-TTS-Provider, X-TTS-Configured, and X-TTS-Fallback communicate
    what actually happened so the frontend can surface a warning if needed.

    Args:
        request: PreviewRequest with persona, voice, provider, optional message,
                 and speed.

    Returns:
        StreamingResponse with Content-Type audio/wav and headers:
        - X-Generation-Time: synthesis latency in milliseconds.
        - X-TTS-Provider: provider that actually generated the audio.
        - X-TTS-Configured: provider that was requested/configured.
        - X-TTS-Fallback: "true" if a fallback provider was used, "false" otherwise.

    Raises:
        400: If provider is unsupported (cloud providers that need full factory machinery).
        502: If the remote Kokoro server is unreachable or returns an error.
        503: If a required local binary (piper, espeak-ng) is not installed.
        504: If synthesis exceeds the timeout.
    """
    # Track which provider was requested and which actually ran, so the headers
    # can accurately reflect whether a fallback occurred.
    configured_provider = request.provider.lower()
    actual_provider = configured_provider
    used_fallback = False

    # ── Proxy to VoxWatch Preview API when needed ────────────────────────────
    # ALL preview requests proxy to the VoxWatch service first.  The
    # dashboard container has no TTS binaries (no espeak, piper, ffmpeg) —
    # only the VoxWatch container has the full AudioPipeline.  Cloud-only
    # providers (ElevenLabs, OpenAI, Cartesia) could technically run here
    # but routing everything through one path keeps voice resolution and
    # fallback behavior consistent.  If VoxWatch is unreachable, we fall
    # through to local cloud-only synthesis as a last resort.
    _should_proxy = True
    if _should_proxy:
        t_proxy_start = time.monotonic()
        proxy_result = await _proxy_dispatch_preview(request)
        if proxy_result is not None:
            wav_bytes, proxy_headers = proxy_result
            elapsed_ms = int((time.monotonic() - t_proxy_start) * 1000)
            logger.info(
                "Preview proxied from VoxWatch: persona=%s provider=%s fallback=%s elapsed_ms=%d bytes=%d",
                request.persona,
                proxy_headers.get("X-TTS-Provider", "?"),
                proxy_headers.get("X-TTS-Fallback", "false"),
                elapsed_ms,
                len(wav_bytes),
            )
            return StreamingResponse(
                content=iter([wav_bytes]),
                media_type="audio/wav",
                headers={
                    "X-Generation-Time": str(elapsed_ms),
                    "X-TTS-Provider": proxy_headers.get("X-TTS-Provider", configured_provider),
                    "X-TTS-Configured": proxy_headers.get("X-TTS-Configured", configured_provider),
                    "X-TTS-Fallback": proxy_headers.get("X-TTS-Fallback", "false"),
                    "Cache-Control": "no-store",
                },
            )
        # VoxWatch unreachable — fall through to local synthesis so the user
        # still hears something (plain TTS without the full dispatch experience).
        logger.info(
            "Dispatch preview falling back to local synthesis for persona=%s",
            request.persona,
        )

    # Resolve the message — prefer the caller's custom text, then persona sample.
    message = (request.message or "").strip() or _sample_for_persona(request.persona)

    # For dispatch personas that fell back to local synthesis, personalize with
    # configured address/agency and normalize 10-codes to spoken form for TTS.
    if request.persona in _DISPATCH_MODES:
        message = await _personalize_dispatch_sample(message)
        message = _normalize_dispatch_for_tts(message)

    t_start = time.monotonic()

    # Use a temp file for the WAV output.  The file is created here and cleaned
    # up after we read it back; NamedTemporaryFile with delete=False is used so
    # the subprocess can write to it by path on all platforms.
