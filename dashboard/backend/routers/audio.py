async def _synthesize_kokoro(
    message: str,
    voice: str,
    speed: float,
    host: str,
    output_path: str,
) -> None:
    """POST to a Kokoro TTS HTTP server and write the WAV response to disk.

    Kokoro's /tts endpoint accepts a JSON body and returns raw PCM/WAV audio.
    The VoxWatch kokoro_provider.py uses the same protocol — this function
    replicates that call so the preview endpoint doesn't need to import the
    full TTS provider machinery.

    Args:
        message:     Text to synthesize.
        voice:       Kokoro voice ID (e.g. "af_heart").
        speed:       Playback speed multiplier.
        host:        Base URL of the Kokoro HTTP server.
        output_path: Absolute path to write the WAV file.

    Raises:
        HTTPException 502: If the Kokoro server is unreachable or returns an error.
    """
    url = f"{host.rstrip('/')}/tts"
    payload = {"text": message, "voice": voice, "speed": speed}

    try:
        async with aiohttp.ClientSession() as session:
            async with session.post(
                url,
                json=payload,
                timeout=aiohttp.ClientTimeout(total=30),
            ) as resp:
                if resp.status != 200:
                    body = await resp.text()
                    raise HTTPException(
                        status_code=status.HTTP_502_BAD_GATEWAY,
                        detail=(
                            f"Kokoro server returned HTTP {resp.status}: {body[:200]}"
                        ),
                    )
                audio_bytes = await resp.read()

        Path(output_path).write_bytes(audio_bytes)

    except aiohttp.ClientError as exc:
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail=f"Cannot reach Kokoro server at {host}: {exc}",
        ) from exc


async def _synthesize_piper(
    message: str,
    model: str,
    speed: float,
    output_path: str,
) -> None:
    """Run piper TTS via subprocess and write the WAV output to disk.

    Piper is invoked as ``piper --model <model> --output_file <path>`` with the
    message piped through stdin.  The model baked into the Docker image is used;
    the ``model`` argument is passed as-is so the preview respects whatever model
    the user has configured.

    Args:
        message:     Text to synthesize.
        model:       Piper model name (e.g. "en_US-lessac-medium").
        speed:       Length scale (1.0 = normal; piper uses length_scale inverse).
        output_path: Absolute path to write the WAV file.

    Raises:
        HTTPException 500: If piper exits with a non-zero return code.
        HTTPException 503: If the piper binary is not found.
    """
    # piper uses --length_scale as the inverse of speed: 1/speed gives the
    # correct duration scaling.  Clamp to avoid extreme values.
    length_scale = round(1.0 / max(speed, 0.25), 3)
    cmd = [
        "piper",
        "--model", model,
        "--length_scale", str(length_scale),
        "--output_file", output_path,
    ]

    try:
        proc = await asyncio.create_subprocess_exec(
            *cmd,
            stdin=asyncio.subprocess.PIPE,
            stdout=asyncio.subprocess.PIPE,
            stderr=asyncio.subprocess.PIPE,
        )
        _, stderr = await asyncio.wait_for(
            proc.communicate(input=message.encode()),
            timeout=30.0,
        )

        if proc.returncode != 0:
            raise HTTPException(
                status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
                detail=f"piper exited with code {proc.returncode}: {stderr.decode()[:300]}",
            )

    except FileNotFoundError:
        raise HTTPException(
            status_code=status.HTTP_503_SERVICE_UNAVAILABLE,
            detail="piper binary not found. Ensure piper is installed in the Docker image.",
        )
    except asyncio.TimeoutError:
        raise HTTPException(
            status_code=status.HTTP_504_GATEWAY_TIMEOUT,
            detail="piper synthesis timed out after 30 seconds.",
        )


async def _synthesize_espeak(
    message: str,
    speed: int,
    pitch: int,
    output_path: str,
) -> None:
    """Run espeak-ng via subprocess and write the WAV output to disk.

    espeak-ng is the always-available last-resort fallback.  It is invoked
    as ``espeak-ng -s <speed> -p <pitch> -w <output_path>`` with the message
    passed as the final argument.

    Args:
        message:     Text to synthesize.
        speed:       Words-per-minute rate (80 – 450).
        pitch:       Voice pitch (0 – 99).
        output_path: Absolute path to write the WAV file.

    Raises:
        HTTPException 503: If espeak-ng is not installed.
        HTTPException 500: On subprocess failure.
    """
    cmd = [
        "espeak-ng",
        "-s", str(speed),
        "-p", str(pitch),
        "-w", output_path,
        message,
    ]

    try:
        proc = await asyncio.create_subprocess_exec(
            *cmd,
            stdout=asyncio.subprocess.PIPE,
            stderr=asyncio.subprocess.PIPE,
        )
        _, stderr = await asyncio.wait_for(
            proc.communicate(),
            timeout=15.0,
        )

        if proc.returncode != 0:
            raise HTTPException(
                status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
                detail=f"espeak-ng exited with code {proc.returncode}: {stderr.decode()[:300]}",
            )

    except FileNotFoundError:
        raise HTTPException(
            status_code=status.HTTP_503_SERVICE_UNAVAILABLE,
            detail="espeak-ng binary not found. Install espeak-ng in the Docker image.",
        )
    except asyncio.TimeoutError:
        raise HTTPException(
            status_code=status.HTTP_504_GATEWAY_TIMEOUT,
            detail="espeak-ng synthesis timed out after 15 seconds.",
        )


async def _resolve_cloud_tts_key(key_name: str) -> str:
    """Resolve a cloud TTS API key from the saved config.

    Reads the flat TTS config (e.g. tts.elevenlabs_api_key) and resolves
    ${ENV_VAR} tokens to actual values.

    Args:
        key_name: The flat config key name (e.g. "elevenlabs_api_key").

    Returns:
        The resolved API key, or empty string if not found.
    """
    import os
    import re
    try:
        raw_cfg = await config_service.get_raw_config()
        tts = raw_cfg.get("tts", {})
        val = tts.get(key_name, "")
        if not val or not isinstance(val, str):
            return ""
        env_match = re.match(r"\$\{(\w+)\}", val)
        if env_match:
            return os.environ.get(env_match.group(1), "")
        if val.startswith("***"):
            return ""
        return val
    except Exception:
        return ""


async def _synthesize_elevenlabs(
    message: str, api_key: str, voice_id: str, model: str, output_path: str,
) -> None:
    """Synthesize audio using ElevenLabs API via raw HTTP.

    No SDK required — uses aiohttp to POST to the text-to-speech endpoint
    and saves the returned audio bytes.

    Args:
        message: Text to synthesize.
        api_key: ElevenLabs API key.
        voice_id: Voice ID from ElevenLabs voice library.
        model: Model name (e.g. "eleven_flash_v2_5").
        output_path: Where to write the output audio file.
    """
    url = f"https://api.elevenlabs.io/v1/text-to-speech/{voice_id}"
    headers = {
        "xi-api-key": api_key,
        "Content-Type": "application/json",
        "Accept": "audio/mpeg",
    }
    payload = {
        "text": message,
        "model_id": model,
        # High stability = monotone, flat delivery (how real dispatchers sound).
        # Low similarity_boost = less voice cloning artifacts.
        # style = 0 removes expressive/emotional inflection entirely.
        "voice_settings": {
            "stability": 1.0,
            "similarity_boost": 0.3,
            "style": 0.0,
            "use_speaker_boost": False,
        },
    }
    async with aiohttp.ClientSession() as session:
        async with session.post(url, json=payload, headers=headers,
                                timeout=aiohttp.ClientTimeout(total=15)) as resp:
            if resp.status == 401:
                raise HTTPException(status_code=400, detail="Invalid ElevenLabs API key")
            if resp.status != 200:
                body = await resp.text()
                raise HTTPException(status_code=502, detail=f"ElevenLabs error: {body[:200]}")
            audio_data = await resp.read()

    # ElevenLabs returns MP3 — convert to WAV for browser playback
    proc = await asyncio.create_subprocess_exec(
        "ffmpeg", "-y", "-i", "pipe:0", "-f", "wav", output_path,
        stdin=asyncio.subprocess.PIPE,
        stdout=asyncio.subprocess.DEVNULL,
        stderr=asyncio.subprocess.DEVNULL,
    )
    await proc.communicate(input=audio_data)


