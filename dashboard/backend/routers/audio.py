# ── VoxWatch Preview API proxy ────────────────────────────────────────────────
#
# Dispatch modes (police_dispatch, tony_montana_dispatch) require the full
# VoxWatch pipeline: channel intro, random chatter, multi-segment TTS, radio
# effects, and officer response.  All of that code lives in the VoxWatch
# service, not the dashboard.  Instead of duplicating it here we proxy the
# request to the VoxWatch Preview API running on port 8892.
#
# The dashboard itself has no VoxWatch port in its own config — we read the
# host from the go2rtc config (same host as the VoxWatch service) and the port
# from ``preview_api_port`` (defaulting to 8892).

#: Response modes that route through the VoxWatch Preview API.
#: Mirrors DISPATCH_MODES in voxwatch/radio_dispatch.py.
_DISPATCH_MODES: frozenset[str] = frozenset({"police_dispatch", "tony_montana_dispatch"})

#: Timeout (seconds) for the VoxWatch preview proxy call.
#: Dispatch previews can take 5-10 s (multiple TTS round-trips + ffmpeg concat).
_VOXWATCH_PROXY_TIMEOUT = 60.0


async def _get_voxwatch_preview_url() -> str:
    """Build the VoxWatch Preview API URL from saved config.

    Reads ``go2rtc.host`` for the hostname (VoxWatch and go2rtc typically run
    on the same host) and ``preview_api_port`` for the port (default 8892).

    Returns:
        Full URL string, e.g. ``"http://192.168.1.10:8892/api/preview"``.
    """
    try:
        cfg = await config_service.get_config()
        host: str = cfg.get("go2rtc", {}).get("host", "localhost")
        port: int = int(cfg.get("preview_api_port", 8892))
    except Exception:
        host = "localhost"
        port = 8892
    return f"http://{host}:{port}/api/preview"


async def _proxy_dispatch_preview(
    request: "PreviewRequest",
) -> Optional[tuple[bytes, dict[str, str]]]:
    """Proxy a preview request to the VoxWatch Preview API.

    Forwards the preview request body to the VoxWatch service on port 8892 and
    returns the raw WAV bytes along with provider tracking headers from the
    VoxWatch response.

    Args:
        request: The dashboard ``PreviewRequest`` model populated by the browser.

    Returns:
        Tuple of (wav_bytes, headers_dict) from the VoxWatch Preview API, or
        ``None`` if the request could not be completed.
    """
    url = await _get_voxwatch_preview_url()

    payload: dict = {
        "response_mode": request.persona,
        "voice": request.voice,
        "provider": request.provider,
        "speed": request.speed,
    }
    if request.message:
        payload["message"] = request.message

    logger.debug(
        "Proxying preview to VoxWatch Preview API: %s mode=%s provider=%s",
        url,
        request.persona,
        request.provider,
    )

    try:
        async with aiohttp.ClientSession() as session:
            async with session.post(
                url,
                json=payload,
                timeout=aiohttp.ClientTimeout(total=_VOXWATCH_PROXY_TIMEOUT),
            ) as resp:
                if resp.status != 200:
                    body_text = await resp.text()
                    logger.warning(
                        "VoxWatch Preview API returned HTTP %d for preview: %s",
                        resp.status,
                        body_text[:200],
                    )
                    return None

                # Forward provider tracking headers from VoxWatch.
                proxy_headers = {
                    "X-Generation-Time-Ms": resp.headers.get("X-Generation-Time-Ms", "0"),
                    "X-TTS-Provider": resp.headers.get("X-TTS-Provider", request.provider),
                    "X-TTS-Configured": resp.headers.get("X-TTS-Configured", request.provider),
                    "X-TTS-Fallback": resp.headers.get("X-TTS-Fallback", "false"),
                }
                elapsed_ms = proxy_headers["X-Generation-Time-Ms"]
                logger.info(
                    "Preview received from VoxWatch: %s ms, mode=%s provider=%s",
                    elapsed_ms,
                    request.persona,
                    proxy_headers["X-TTS-Provider"],
                )
                wav_bytes = await resp.read()
                return (wav_bytes, proxy_headers)

    except aiohttp.ClientConnectorError as exc:
        logger.info(
            "VoxWatch Preview API unreachable at %s (%s) — falling back to local synthesis.",
            url,
            exc,
        )
        return None
    except asyncio.TimeoutError:
        logger.warning(
            "VoxWatch Preview API timed out after %.0fs — falling back to local synthesis.",
            _VOXWATCH_PROXY_TIMEOUT,
        )
        return None
    except Exception as exc:
        logger.warning(
            "VoxWatch Preview API proxy error: %s — falling back to local synthesis.",
            exc,
        )
        return None


