@router.post(
    "/preview",
    summary="Generate browser-playable audio preview",
    description=(
        "Synthesizes a deterrent message using the specified TTS provider and persona, "
        "then returns the raw WAV audio for in-browser playback. "
        "No camera speaker is involved — this is a pure preview for voice selection. "
        "Supported providers: kokoro (remote HTTP server), piper (local), espeak (local fallback). "
        "Response headers X-TTS-Provider, X-TTS-Configured, and X-TTS-Fallback indicate which "
        "provider actually generated the audio and whether a fallback occurred."
    ),
    response_class=StreamingResponse,
    responses={
        200: {
            "content": {"audio/wav": {}},
            "description": "Raw WAV audio. Play with: new Audio(URL.createObjectURL(blob))",
            "headers": {
                "X-Generation-Time": {
                    "description": "Synthesis latency in milliseconds",
                    "schema": {"type": "string"},
                },
                "X-TTS-Provider": {
                    "description": "TTS provider that actually generated the audio",
                    "schema": {"type": "string"},
                },
                "X-TTS-Configured": {
                    "description": "TTS provider that was requested/configured",
                    "schema": {"type": "string"},
                },
                "X-TTS-Fallback": {
                    "description": "Whether a fallback provider was used ('true' or 'false')",
                    "schema": {"type": "string"},
                },
            },
        },
        400: {"description": "Unsupported provider or bad request"},
        502: {"description": "Remote TTS server error"},
        503: {"description": "Local TTS binary not available"},
    },
)
async def preview_audio(request: PreviewRequest) -> StreamingResponse:
    """Synthesize a deterrent message sample and stream WAV back to the browser.

    The caller selects a persona and TTS provider.  If no custom message is
    supplied, a pre-written sample for that persona is used so the user hears
    a realistic deterrent rather than a bland test phrase.

    Synthesis is performed inside a temporary file so the WAV can be read back
    after the subprocess/HTTP call finishes.  The temp file is deleted after
    the response is streamed.

    For cloud providers (ElevenLabs, OpenAI, Cartesia), if synthesis fails due
    to a missing or invalid API key, the endpoint falls back to espeak rather
    than returning an error, so the user always hears something.  The response
    headers X-TTS-Provider, X-TTS-Configured, and X-TTS-Fallback communicate
    what actually happened so the frontend can surface a warning if needed.

    Args:
        request: PreviewRequest with persona, voice, provider, optional message,
                 and speed.

    Returns:
        StreamingResponse with Content-Type audio/wav and headers:
        - X-Generation-Time: synthesis latency in milliseconds.
        - X-TTS-Provider: provider that actually generated the audio.
        - X-TTS-Configured: provider that was requested/configured.
        - X-TTS-Fallback: "true" if a fallback provider was used, "false" otherwise.

    Raises:
        400: If provider is unsupported (cloud providers that need full factory machinery).
        502: If the remote Kokoro server is unreachable or returns an error.
        503: If a required local binary (piper, espeak-ng) is not installed.
        504: If synthesis exceeds the timeout.
    """
    # Track which provider was requested and which actually ran, so the headers
    # can accurately reflect whether a fallback occurred.
    configured_provider = request.provider.lower()
    actual_provider = configured_provider
    used_fallback = False

    # ── Proxy to VoxWatch Preview API when needed ────────────────────────────
    # ALL preview requests proxy to the VoxWatch service first.  The
    # dashboard container has no TTS binaries (no espeak, piper, ffmpeg) —
    # only the VoxWatch container has the full AudioPipeline.  Cloud-only
    # providers (ElevenLabs, OpenAI, Cartesia) could technically run here
    # but routing everything through one path keeps voice resolution and
    # fallback behavior consistent.  If VoxWatch is unreachable, we fall
    # through to local cloud-only synthesis as a last resort.
    _should_proxy = True
    if _should_proxy:
        t_proxy_start = time.monotonic()
        proxy_result = await _proxy_dispatch_preview(request)
        if proxy_result is not None:
            wav_bytes, proxy_headers = proxy_result
            elapsed_ms = int((time.monotonic() - t_proxy_start) * 1000)
            logger.info(
                "Preview proxied from VoxWatch: persona=%s provider=%s fallback=%s elapsed_ms=%d bytes=%d",
                request.persona,
                proxy_headers.get("X-TTS-Provider", "?"),
                proxy_headers.get("X-TTS-Fallback", "false"),
                elapsed_ms,
                len(wav_bytes),
            )
            resp_headers = {
                "X-Generation-Time": str(elapsed_ms),
                "X-TTS-Provider": proxy_headers.get("X-TTS-Provider", configured_provider),
                "X-TTS-Configured": proxy_headers.get("X-TTS-Configured", configured_provider),
                "X-TTS-Fallback": proxy_headers.get("X-TTS-Fallback", "false"),
                "X-VoxWatch-Proxy": "proxied",
                "Cache-Control": "no-store",
            }
            # Forward the fallback reason so the frontend can display it.
            fallback_reason = proxy_headers.get("X-TTS-Fallback-Reason", "")
            if fallback_reason:
                resp_headers["X-TTS-Fallback-Reason"] = fallback_reason
            return StreamingResponse(
                content=iter([wav_bytes]),
                media_type="audio/wav",
                headers=resp_headers,
            )
        # VoxWatch unreachable — fall through to local synthesis so the user
        # still hears something (plain TTS without the full dispatch experience).
        logger.info(
            "Dispatch preview falling back to local synthesis for persona=%s",
            request.persona,
        )

    # Resolve the message — prefer the caller's custom text, then persona sample.
    message = (request.message or "").strip() or _sample_for_persona(request.persona)
    # Strip Unicode control characters before passing to any TTS subprocess.
    message = _sanitize_tts_message(message)

    # Validate piper model name if the provider is piper — prevent path traversal
    # in the --model argument even though create_subprocess_exec is shell-safe.
    if configured_provider == "piper" and request.voice and not _PIPER_MODEL_RE.match(request.voice):
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Invalid Piper model name. Only alphanumeric, hyphens, and underscores allowed.",
        )

    # For dispatch personas that fell back to local synthesis, personalize with
    # configured address/agency and normalize 10-codes to spoken form for TTS.
    if request.persona in _DISPATCH_MODES:
        message = await _personalize_dispatch_sample(message)
        message = _normalize_dispatch_for_tts(message)

    t_start = time.monotonic()

    # Use a temp file for the WAV output.  The file is created here and cleaned
    # up after we read it back; NamedTemporaryFile with delete=False is used so
    # the subprocess can write to it by path on all platforms.
    with tempfile.NamedTemporaryFile(suffix=".wav", delete=False) as tmp:
        tmp_path = tmp.name

    try:
        provider = request.provider.lower()

        if provider == "kokoro":
            # Kokoro requires a host URL.  Fall back to reading from config if
            # the caller didn't supply one (so users who have it saved in config
            # don't need to re-enter it).
            host = request.provider_host
            if not host:
                try:
                    cfg = await config_service.get_config()
                    host = cfg.get("tts", {}).get("kokoro_host") or None
                except Exception:
                    host = None

            if not host:
                raise HTTPException(
                    status_code=status.HTTP_400_BAD_REQUEST,
                    detail=(
                        "Kokoro host URL is required for preview. "
                        "Set it in TTS config or pass provider_host in the request."
                    ),
                )

            await _synthesize_kokoro(
                message=message,
                voice=request.voice,
                speed=request.speed,
                host=host,
                output_path=tmp_path,
            )

        elif provider == "piper":
            # voice field doubles as the model name for piper.
            # Piper runs inside the VoxWatch container, not the dashboard.
            # If piper isn't available here, fall back to espeak for preview.
            model = request.voice or "en_US-lessac-medium"
            try:
                await _synthesize_piper(
                    message=message,
                    model=model,
                    speed=request.speed,
                    output_path=tmp_path,
                )
            except HTTPException as exc:
                if exc.status_code == 503:
                    # Piper not installed in this container — use espeak fallback
                    logger.info(
                        "Piper not available in dashboard container, "
                        "falling back to espeak for preview"
                    )
                    actual_provider = "espeak"
                    used_fallback = True
                    wpm = int(min(450, max(80, 175 * request.speed)))
                    await _synthesize_espeak(
                        message=message,
                        speed=wpm,
                        pitch=50,
                        output_path=tmp_path,
                    )
                else:
                    raise

        elif provider == "espeak":
            # Speed for espeak is words-per-minute, not a multiplier.
            # Convert: 1.0 = 175 WPM (piper/kokoro normal), scale linearly.
            wpm = int(min(450, max(80, 175 * request.speed)))
            await _synthesize_espeak(
                message=message,
                speed=wpm,
                pitch=50,
                output_path=tmp_path,
            )

        elif provider == "elevenlabs":
            # Attempt ElevenLabs synthesis.  If the API key is missing or the
            # call fails, fall back to espeak so the user hears something and
            # the headers explain what happened.
            api_key = await _resolve_cloud_tts_key("elevenlabs_api_key")
            if not api_key:
                logger.warning(
                    "ElevenLabs API key not configured — falling back to espeak for preview. "
                    "Set tts.elevenlabs_api_key in config.yaml to enable ElevenLabs preview."
                )
                actual_provider = "espeak"
                used_fallback = True
                wpm = int(min(450, max(80, 175 * request.speed)))
                await _synthesize_espeak(
                    message=message,
                    speed=wpm,
                    pitch=50,
                    output_path=tmp_path,
                )
            else:
                actual_provider, used_fallback = await _try_with_espeak_fallback(
                    synthesize_fn=lambda: _synthesize_elevenlabs(
                        message=message,
                        api_key=api_key,
                        voice_id=request.voice or "pNInz6obpgDQGcFmaJgB",
                        model="eleven_flash_v2_5",
                        output_path=tmp_path,
                    ),
                    message=message,
                    speed=request.speed,
                    output_path=tmp_path,
                    tmp_path=tmp_path,
                    provider_name="elevenlabs",
                )

        elif provider == "openai":
            # Same fallback pattern as ElevenLabs.
            api_key = await _resolve_cloud_tts_key("openai_api_key")
            if not api_key:
                logger.warning(
                    "OpenAI API key not configured — falling back to espeak for preview. "
                    "Set tts.openai_api_key in config.yaml to enable OpenAI TTS preview."
                )
                actual_provider = "espeak"
                used_fallback = True
                wpm = int(min(450, max(80, 175 * request.speed)))
                await _synthesize_espeak(
                    message=message,
                    speed=wpm,
                    pitch=50,
                    output_path=tmp_path,
                )
            else:
                actual_provider, used_fallback = await _try_with_espeak_fallback(
                    synthesize_fn=lambda: _synthesize_openai_tts(
                        message=message,
                        api_key=api_key,
                        voice=request.voice or "onyx",
                        model="tts-1",
                        speed=request.speed,
                        output_path=tmp_path,
                    ),
                    message=message,
                    speed=request.speed,
                    output_path=tmp_path,
                    tmp_path=tmp_path,
                    provider_name="openai",
                )

        elif provider == "cartesia":
            # Same fallback pattern as ElevenLabs.
            api_key = await _resolve_cloud_tts_key("cartesia_api_key")
            if not api_key:
                logger.warning(
                    "Cartesia API key not configured — falling back to espeak for preview. "
                    "Set tts.cartesia_api_key in config.yaml to enable Cartesia preview."
                )
                actual_provider = "espeak"
                used_fallback = True
                wpm = int(min(450, max(80, 175 * request.speed)))
                await _synthesize_espeak(
                    message=message,
                    speed=wpm,
                    pitch=50,
                    output_path=tmp_path,
                )
            else:
                actual_provider, used_fallback = await _try_with_espeak_fallback(
                    synthesize_fn=lambda: _synthesize_cartesia(
                        message=message,
                        api_key=api_key,
                        voice_id=request.voice or "",
                        output_path=tmp_path,
                    ),
                    message=message,
                    speed=request.speed,
                    output_path=tmp_path,
                    tmp_path=tmp_path,
                    provider_name="cartesia",
                )

        else:
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail=f"Provider '{provider}' is not supported for browser preview.",
            )

        # Post-process: apply a lightweight radio static effect for dispatch
        # personas that fell back to local synthesis (i.e. VoxWatch was down).
        # The full radio pipeline (apply_radio_effect in audio_effects.py) is
        # only available when VoxWatch is running; the ffmpeg filter below is a
        # simplified fallback that keeps the local preview from sounding too clean.
        if request.persona in _DISPATCH_MODES:
            radio_path = tmp_path + ".radio.wav"
            try:
                radio_proc = await asyncio.create_subprocess_exec(
                    "ffmpeg", "-y",
                    # Input 0: the clean TTS voice
                    "-i", tmp_path,
                    # Input 1: white noise for radio static
                    "-f", "lavfi", "-i", "anoisesrc=d=60:c=white:a=0.02",
                    # Input 2: dispatch activation beep (1kHz, 0.15s)
                    "-f", "lavfi", "-i", "sine=frequency=1000:duration=0.5,volume=0.3",
                    "-filter_complex",
                    # Process voice: narrow bandpass (radio bandwidth), compress,
                    # mix with static noise. Then prepend the dispatch beep.
                    "[0:a]highpass=f=400,lowpass=f=2500,"
                    "compand=.3|.3:1|1:-90/-60|-60/-40|-40/-30|-20/-20:6:0:-90:0.2,"
                    "volume=0.8[voice];"
                    "[1:a]volume=0.15[noise];"
                    "[voice][noise]amix=inputs=2:duration=first:dropout_transition=0[mixed];"
                    "[2:a][mixed]concat=n=2:v=0:a=1[out]",
                    "-map", "[out]",
                    radio_path,
                    stdout=asyncio.subprocess.PIPE,
                    stderr=asyncio.subprocess.PIPE,
                )
                _, stderr_bytes = await asyncio.wait_for(radio_proc.communicate(), timeout=10.0)
                if radio_proc.returncode == 0 and Path(radio_path).exists():
                    # Swap the radio-processed file in
                    Path(tmp_path).unlink(missing_ok=True)
                    Path(radio_path).rename(tmp_path)
                    logger.info("Applied police radio static effect")
                else:
                    stderr_text = stderr_bytes.decode("utf-8", errors="replace")[-300:]
                    logger.warning("Radio effect ffmpeg failed (rc=%s): %s",
                                   radio_proc.returncode, stderr_text)
            except Exception as exc:
                logger.debug("Radio effect post-processing failed: %s — using clean audio", exc)
            finally:
                Path(radio_path).unlink(missing_ok=True)

        elapsed_ms = int((time.monotonic() - t_start) * 1000)

        # Read the WAV into memory and delete the temp file.
        wav_bytes = Path(tmp_path).read_bytes()

        logger.info(
            "Audio preview generated: provider=%s (configured=%s fallback=%s) "
            "persona=%s voice=%s message_len=%d elapsed_ms=%d",
            actual_provider,
            configured_provider,
            used_fallback,
            request.persona,
            request.voice,
            len(message),
            elapsed_ms,
        )

        return StreamingResponse(
            content=iter([wav_bytes]),
            media_type="audio/wav",
            headers={
                "X-Generation-Time": str(elapsed_ms),
                "X-TTS-Provider": actual_provider,
                "X-TTS-Configured": configured_provider,
                "X-TTS-Fallback": "true" if used_fallback else "false",
                "X-VoxWatch-Proxy": "local-fallback",
                "Cache-Control": "no-store",
            },
        )

    finally:
        # Always clean up the temp file, even if an exception was raised.
        try:
            Path(tmp_path).unlink(missing_ok=True)
        except Exception:
            pass
