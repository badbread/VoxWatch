        dispatch = rm.get("dispatch", {})

        address = dispatch.get("full_address", "").strip()
        agency = dispatch.get("agency", "").strip()
        callsign = dispatch.get("callsign", "").strip()

        if address:
            # Build a personalized sample
            agency_prefix = f"{agency} dispatch. " if agency else ""
            callsign_suffix = f" {callsign}, respond code 3." if callsign else " Requesting unit respond."
