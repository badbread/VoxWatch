# ── espeak fallback helper (R4) ───────────────────────────────────────────────


async def _try_with_espeak_fallback(
    synthesize_fn,
    message: str,
    speed: float,
    output_path: str,
    tmp_path: str,
    provider_name: str,
) -> tuple[str, bool]:
    """Try a cloud provider, falling back to espeak on failure.

    Attempts ``synthesize_fn`` (a coroutine that writes audio to
    ``output_path``).  If the call raises an ``HTTPException`` the error is
    logged and espeak is invoked instead as a last-resort fallback so the
    user always hears something.

    Args:
        synthesize_fn: Async callable that synthesizes audio to ``output_path``.
                       Must be a zero-argument coroutine (close over args via
                       ``functools.partial`` or a lambda).
        message:       TTS input text (used for the espeak fallback).
        speed:         Speed multiplier (used to compute espeak WPM).
        output_path:   Destination path for the audio file.
        tmp_path:      Same path as output_path — kept as a named parameter
                       for clarity; the espeak call writes here on fallback.
        provider_name: Human-readable provider label used in log messages.

    Returns:
        Tuple of ``(actual_provider, used_fallback)`` where ``actual_provider``
        is either ``provider_name`` (success) or ``"espeak"`` (fallback), and
        ``used_fallback`` is ``True`` when espeak was substituted.
    """
    try:
        await synthesize_fn()
        return (provider_name, False)
    except HTTPException as exc:
        logger.warning(
            "%s synthesis failed (HTTP %d: %s) — falling back to espeak for preview.",
            provider_name,
            exc.status_code,
            exc.detail,
        )
        wpm = int(min(450, max(80, 175 * speed)))
        await _synthesize_espeak(
            message=message,
            speed=wpm,
            pitch=50,
            output_path=tmp_path,
        )
        return ("espeak", True)


